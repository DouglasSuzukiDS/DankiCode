# python -m venv NAME_PAST => Cria a pasta de embiente virtual

# Necessário 'ativar' o ambient em NAME_PAST/Scripts/activate.bat (source env/Scripts/activate)

# pip install django => Instala o Django no ambiente virtual

# pip freeze > requirements.txt => Armazena todas as dependências do projeto

# django-admin startproject NAME . => Cria o projeto Django na pasta atual

# python manage.py runserver => Inicia o servidor de desenvolvimento do Django

# python manage.py migrate => Aplica as migrações do banco de dados

# python manage.py createsuperuser => Cria um superusuário para acessar o admin do Django

# localhost:8000 => Acessa o servidor de desenvolvimento no navegador

# localhost:8000/admin => Acessa o painel de administração do Django

# python manage.py startapp NAME => Cria um novo aplicativo Django dentro do projeto

# python manage.py makemigrations => Cria arquivos de migração para o banco de dados com base nas alterações nos modelos